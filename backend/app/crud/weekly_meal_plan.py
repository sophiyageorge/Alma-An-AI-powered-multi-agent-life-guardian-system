def delete_meal_plan(
    db: Session,
    meal_plan_id: int
) -> bool:
    """
    Delete a meal plan.

    Args:
        db: Database session
        meal_plan_id: Meal plan ID

    Returns:
        True if deleted successfully
    """

    try:
        logger.info(f"Deleting meal plan | id={meal_plan_id}")

        meal_plan = (
            db.query(WeeklyMealPlan)
            .filter(WeeklyMealPlan.id == meal_plan_id)
            .first()
        )

        if not meal_plan:
            logger.warning(f"Meal plan not found | id={meal_plan_id}")
            return False

        db.delete(meal_plan)
        db.commit()

        logger.info(f"Meal plan deleted | id={meal_plan_id}")

        return True

    except SQLAlchemyError as e:
        db.rollback()
        logger.error(f"Error deleting meal plan: {str(e)}")
        raise NutritionAgentError("Failed to delete meal plan")
