def create_weekly_meal_plan(
    db: Session,
    user_id: int,
    calories: Optional[int],
    diet: Optional[str],
    region: Optional[str],
    goal: Optional[str],
    restrictions: Optional[List[str]],
    meal_plan_text: str
) -> WeeklyMealPlan:
    """
    Create and store a new weekly meal plan generated by the Nutrition Agent.

    Args:
        db: Database session
        user_id: User identifier
        calories: Target calorie intake
        diet: Diet preference
        region: Cuisine region
        goal: Health goal
        restrictions: Dietary restrictions
        meal_plan_text: AI generated weekly meal plan

    Returns:
        WeeklyMealPlan object
    """

    try:
        logger.info(f"Creating weekly meal plan | user_id={user_id}")

        meal_plan = WeeklyMealPlan(
            user_id=user_id,
            calories=calories,
            diet=diet,
            region=region,
            goal=goal,
            restrictions=restrictions,
            meal_plan_text=meal_plan_text
        )

        db.add(meal_plan)
        db.commit()
        db.refresh(meal_plan)

        # Log creation
        meal_plan.log_creation()

        logger.info(f"Meal plan stored successfully | plan_id={meal_plan.id}")

        return meal_plan

    except SQLAlchemyError as e:
        db.rollback()
        logger.error(f"Database error while creating meal plan: {str(e)}")
        raise NutritionAgentError("Failed to create weekly meal plan")


