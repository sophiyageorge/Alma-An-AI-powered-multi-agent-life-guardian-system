def get_current_week_meal_plan(
    db: Session,
    user_id: int
) -> Optional[WeeklyMealPlan]:
    """
    Fetch the meal plan created for the current week for a user.

    This function ensures that only one meal plan exists
    per user per week.

    Args:
        db: Database session
        user_id: User identifier

    Returns:
        WeeklyMealPlan if exists, otherwise None
    """

    try:
        logger.info(f"Fetching current week meal plan | user_id={user_id}")

        # ---------------------------------------------------------
        # Calculate start and end of current week (Monday → Sunday)
        # ---------------------------------------------------------

        today = datetime.utcnow()

        start_of_week = today - timedelta(days=today.weekday())
        start_of_week = start_of_week.replace(
            hour=0, minute=0, second=0, microsecond=0
        )

        end_of_week = start_of_week + timedelta(days=7)

        logger.debug(
            f"Week range | start={start_of_week} | end={end_of_week}"
        )

        # ---------------------------------------------------------
        # Query database
        # ---------------------------------------------------------

        meal_plan = (
            db.query(WeeklyMealPlan)
            .filter(
                WeeklyMealPlan.user_id == user_id,
                WeeklyMealPlan.created_at >= start_of_week,
                WeeklyMealPlan.created_at < end_of_week
            )
            .order_by(WeeklyMealPlan.created_at.desc())
            .first()
        )

        if meal_plan:
            logger.info(
                f"Current week meal plan found | plan_id={meal_plan.id}"
            )
        else:
            logger.info(
                f"No meal plan found for current week | user_id={user_id}"
            )

        return meal_plan

    except SQLAlchemyError as e:
        logger.error(f"Error fetching weekly meal plan: {str(e)}")
        raise NutritionAgentError("Failed to fetch current week meal plan")

