logger = setup_logger(__name__)


def approve_meal_plan(
    db: Session,
    meal_plan_id: int,
    shop_number: Optional[str] = None
) -> Optional[WeeklyMealPlan]:
    """
    Approve a weekly meal plan and optionally set the grocery shop number.

    Args:
        db (Session): Database session.
        meal_plan_id (int): ID of the meal plan to approve.
        shop_number (str, optional): Grocery store number to associate.

    Returns:
        WeeklyMealPlan | None: Updated meal plan or None if not found.
    """
    try:
        logger.info("Approving meal plan | plan_id=%s", meal_plan_id)

        meal_plan = db.query(WeeklyMealPlan).filter(WeeklyMealPlan.id == meal_plan_id).first()
        if not meal_plan:
            logger.warning("Meal plan not found | plan_id=%s", meal_plan_id)
            return None

        # Update approval and shop number
        meal_plan.is_approved = True
        if shop_number:
            meal_plan.shop_number = shop_number

        db.commit()
        db.refresh(meal_plan)

        logger.info("Meal plan approved | plan_id=%s | shop_number=%s", meal_plan_id, shop_number)
        return meal_plan

    except SQLAlchemyError as e:
        db.rollback()
        logger.exception("Error approving meal plan | plan_id=%s", meal_plan_id)
        raise NutritionAgentError("Failed to approve meal plan") from e


def get_current_week_grocery_list(db: Session, user_id: int) -> Optional[dict]:
    """
    Fetch the grocery list for the current week's approved meal plan.

    Args:
        db (Session): Database session.
        user_id (int): User ID.

    Returns:
        dict | None: Grocery list dictionary if exists, otherwise None.
    """
    try:
        today = datetime.utcnow().date()
        week_start = today - timedelta(days=today.weekday())
        week_end = week_start + timedelta(days=7)

        meal_plan = (
            db.query(WeeklyMealPlan)
            .filter(
                WeeklyMealPlan.user_id == user_id,
                WeeklyMealPlan.is_approved == True,
                WeeklyMealPlan.created_at >= week_start,
                WeeklyMealPlan.created_at < week_end
            )
            .order_by(WeeklyMealPlan.created_at.desc())
            .first()
        )

        if not meal_plan:
            logger.info("No approved meal plan found for current week | user_id=%s", user_id)
            return None

        logger.info(
            "Fetched current week's grocery list | user_id=%s | plan_id=%s",
            user_id,
            meal_plan.id
        )

        return {
            "meal_plan_id": meal_plan.id,
            "shop_number": meal_plan.shop_number,
            "grocery_list": meal_plan.grocery_list
        }

    except SQLAlchemyError as e:
        logger.exception("Error fetching grocery list for user_id=%s", user_id)
        return None


def update_grocery_list(
    db: Session,
    meal_plan_id: int,
    grocery_list: Dict,
    shop_number: Optional[str] = None
) -> Optional[WeeklyMealPlan]:
    """
    Update grocery list generated by Grocery Agent.

    Args:
        db: Database session
        meal_plan_id: Meal plan ID
        grocery_list: Structured grocery list
        shop_number: Optional store reference

    Returns:
        Updated WeeklyMealPlan
    """

    try:
        logger.info(f"Updating grocery list | plan_id={meal_plan_id}")

        meal_plan = (
            db.query(WeeklyMealPlan)
            .filter(WeeklyMealPlan.id == meal_plan_id)
            .first()
        )

        if not meal_plan:
            logger.warning(f"Meal plan not found | id={meal_plan_id}")
            return None

        meal_plan.grocery_list = grocery_list
        meal_plan.shop_number = shop_number

        db.commit()
        db.refresh(meal_plan)

        logger.info(f"Grocery list updated | plan_id={meal_plan_id}")

        return meal_plan

    except SQLAlchemyError as e:
        db.rollback()
        logger.error(f"Error updating grocery list: {str(e)}")
        raise NutritionAgentError("Failed to update grocery list")


